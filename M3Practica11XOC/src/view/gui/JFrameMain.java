    private void jButtonValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValidateActionPerformed
        String Name = jTextFieldName.getText();
        String NIF = jTextFieldDNI.getText();
        String Email = jTextFieldEmail.getText();
        String Birth = jTextFieldBirthdate.getText();
        String ZIP = jTextFieldZIP.getText();
        int age = 0;
        boolean error = false;

        try {
            UserDataValidations.checkName(Name);  // Llamamos a la funci√≥n de validaci√≥n del nombre
            jTextFieldName.setEditable(false);
            jLabelErrName.setText("");  // Limpiar el mensaje de error si el nombre es v√°lido
        } catch (InvalidNameException ex) {
            jLabelErrName.setText(ex.getMessage());  // Mostrar el mensaje de la excepci√≥n en el label de error
            error = true;
        }

        
        try {
            UserDataValidations.checkId(1, NIF);  // Verificamos el NIF
            jTextFieldDNI.setEditable(false);
            jLabelErrDNI.setText("");  // Limpiar el mensaje de error si el NIF es v√°lido
        } catch (InvalidDNIException ex) {
            jLabelErrDNI.setText(ex.getMessage());  // Mostrar el mensaje de la excepci√≥n en el label de error
            error = true;
        }

        if (!UserDataValidations.checkEmail(Email)) {
            jLabelErrEmail.setText("incorrect e-mail!");
            error = true;
        } else {
            jTextFieldEmail.setEditable(false);
            jLabelErrEmail.setText("");
        }

        if (!UserDataValidations.checkFormatDate(Birth)) {
            jLabelErrBirthdate.setText("incorrect birthday!");
            error = true;
        } else {
            age = UserDataValidations.calculateAge(Birth);
            jTextFieldAge.setText(String.valueOf(age));
            jTextFieldAge.setEditable(false);
            jTextFieldBirthdate.setEditable(false);
            jLabelErrBirthdate.setText("");
        }

        if (!UserDataValidations.checkPostalCode(ZIP)) {
            jLabelErrZIP.setText("incorrect ZIP!");
            error = true;
        } else {
            jTextFieldZIP.setEditable(false);
            jLabelErrZIP.setText("");
        }

        jButtonCSV.setEnabled(!error);

        // TODO add your handling code here:

    }//GEN-LAST:event_jButtonValidateActionPerformed

